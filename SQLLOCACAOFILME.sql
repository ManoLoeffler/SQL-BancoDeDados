CREATE TABLE CLIENTE (
  CD_CLIENTE           INTEGER NOT NULL,       
  NOME                 VARCHAR(20) NOT NULL,       
  ENDERECO             VARCHAR(80),       
  TELEFONE             VARCHAR(20),       
  CONSTRAINT XPKCLIENTE               
  PRIMARY KEY (CD_CLIENTE) );
  
CREATE TABLE LOCACAO (
       NR_LOCACAO           INTEGER NOT NULL,       
		 DT_LOCACAO           DATE NOT NULL,       
		 CD_CLIENTE              INTEGER NOT NULL,       
		 CONSTRAINT XPKLOCACAO               
		 PRIMARY KEY (NR_LOCACAO),        
		 CONSTRAINT FK_CLIENTE_LOCACAO              
		 FOREIGN KEY (CD_CLIENTE)                             
		 REFERENCES CLIENTE );
		 
CREATE TABLE TIPO (       
		CD_TIPO              INTEGER NOT NULL,       
		DS_TIPO              VARCHAR(20) NOT NULL,       
		VL_TIPO              DOUBLE PRECISION NOT NULL,       
		CONSTRAINT XPKTIPO               
		PRIMARY KEY (CD_TIPO) );
		
CREATE TABLE GENERO (       
	CD_GENERO            INTEGER NOT NULL,       
	DS_GENERO            VARCHAR(60) NOT NULL,       
	CONSTRAINT XPKGENERO               
	PRIMARY KEY (CD_GENERO) );
	
CREATE TABLE FILME (       
	CD_FILME             INTEGER NOT NULL,       
	CD_TIPO              INTEGER,       
	NM_FILME             VARCHAR(20) NOT NULL,       
	CD_GENERO            INTEGER NOT NULL,       
	NR_MIDIAS            INTEGER,       
	CONSTRAINT XPKFILME               
	PRIMARY KEY (CD_FILME),        
	CONSTRAINT FK_TIPO_FILME              
	FOREIGN KEY (CD_TIPO)                             
	REFERENCES TIPO,        
	CONSTRAINT FK_GENERO_FILME              
	FOREIGN KEY (CD_GENERO)                             
	REFERENCES GENERO );
		
	CREATE TABLE FILME_LOCACAO (       
	   CD_FILME             INTEGER NOT NULL,       
		NR_LOCACAO           INTEGER NOT NULL,       
		DT_PARADEV           DATE NOT NULL,       
		DT_DEVOLVIDO         DATE,       
		CONSTRAINT XPKFILME_LOCACAO               
		PRIMARY KEY (CD_FILME, NR_LOCACAO),        
		CONSTRAINT FK_LOCACAO_FILMELOCACAO              
		FOREIGN KEY (NR_LOCACAO)                             
		REFERENCES LOCACAO,
		CONSTRAINT FK_FILME_FILMELOCACAO              
		FOREIGN KEY (CD_FILME)                             
		REFERENCES FILME );
		
